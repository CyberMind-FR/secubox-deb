def _run_cmd(cmd: List[str], timeout: int = 30) -> dict:
    """Run a command and return result."""
    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout
        )
        return {
            "success": result.returncode == 0,
            "stdout": result.stdout,
            "stderr": result.stderr,
            "returncode": result.returncode
        }
    except subprocess.TimeoutExpired:
        return {"success": False, "error": "Command timed out"}
    except Exception as e:
        return {"success": False, "error": str(e)}


def _service_status() -> dict:
    """Get coturn service status."""
    result = subprocess.run(
        ["systemctl", "is-active", "coturn"],
        capture_output=True,
        text=True
    )
    active = result.stdout.strip() == "active"

    enabled_result = subprocess.run(
        ["systemctl", "is-enabled", "coturn"],
        capture_output=True,
        text=True
    )
    enabled = enabled_result.stdout.strip() == "enabled"

    # Get process info if running
    pid = None
    uptime = None
    if active:
        pid_result = subprocess.run(
            ["pgrep", "-x", "turnserver"],
            capture_output=True,
            text=True
        )
        if pid_result.returncode == 0:
            pid = int(pid_result.stdout.strip().split()[0])
            # Get uptime from /proc
            try:
                stat = Path(f"/proc/{pid}/stat").read_text()
                starttime = int(stat.split()[21])
                uptime_sec = int(Path("/proc/uptime").read_text().split()[0])
                boot_time = time.time() - uptime_sec
                start_time = boot_time + (starttime / 100)
                uptime = int(time.time() - start_time)
            except Exception:
                pass

    return {
        "active": active,
        "enabled": enabled,
        "pid": pid,
        "uptime_seconds": uptime
    }


def _get_config() -> dict:
    """Parse turnserver.conf into dict."""
    config = {}
    if TURNSERVER_CONF.exists():
        for line in TURNSERVER_CONF.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                key, value = line.split("=", 1)
                config[key.strip()] = value.strip()
            else:
                config[line] = True
    return config


def _get_users() -> List[dict]:
    """Get TURN users from users.json."""
    if USERS_DB.exists():
        try:
            return json.loads(USERS_DB.read_text())
        except Exception:
            pass
    return []


def _save_users(users: List[dict]):
    """Save TURN users to users.json."""
    USERS_DB.parent.mkdir(parents=True, exist_ok=True)
    USERS_DB.write_text(json.dumps(users, indent=2))


def _get_realm() -> str:
    """Get current realm from config."""
    config = _get_config()
    return config.get("realm", "turn.secubox.local")


def _get_secret() -> str:
    """Get static auth secret from config."""
    config = _get_config()
    return config.get("static-auth-secret", "")


def _generate_temp_credentials(username: str, ttl: int = 86400) -> dict:
    """Generate temporary TURN credentials using HMAC."""
    secret = _get_secret()
    if not secret:
        return {"error": "No static-auth-secret configured"}

    timestamp = int(time.time()) + ttl
    temp_username = f"{timestamp}:{username}"

    # HMAC-SHA1 password
    password = hmac.new(
        secret.encode(),
        temp_username.encode(),
        hashlib.sha1
    ).digest()

    import base64
    password_b64 = base64.b64encode(password).decode()

    realm = _get_realm()
    config = _get_config()
    port = config.get("listening-port", "3478")
    tls_port = config.get("tls-listening-port", "5349")

    return {
        "username": temp_username,
        "password": password_b64,
        "ttl": ttl,
        "expires": timestamp,
        "realm": realm,
        "turn_urls": [
            f"turn:{realm}:{port}",
            f"turn:{realm}:{port}?transport=tcp",
            f"turns:{realm}:{tls_port}",
        ],
        "stun_urls": [
            f"stun:{realm}:{port}",
        ]
    }


def _parse_sessions_from_cli() -> List[dict]:
    """Parse active sessions from turnserver CLI or logs."""
    sessions = []

    # Try to get sessions from CLI if enabled
    result = subprocess.run(
        ["ss", "-tunp"],
        capture_output=True,
        text=True
    )

    if result.returncode == 0:
        for line in result.stdout.splitlines():
            if "turnserver" in line:
                parts = line.split()
                if len(parts) >= 5:
                    # Parse local and remote addresses
                    local = parts[4] if len(parts) > 4 else "-"
                    remote = parts[5] if len(parts) > 5 else "-"
                    sessions.append({
                        "protocol": parts[0],
                        "local_addr": local,
                        "remote_addr": remote,
                        "state": parts[1] if len(parts) > 1 else "UNKNOWN"
                    })

    return sessions


def _get_stats_from_logs() -> dict:
    """Parse statistics from turnserver logs."""
    stats = {
        "total_allocations": 0,
        "active_allocations": 0,
        "total_data_relayed": 0,
        "auth_failures": 0
    }

    log_file = Path("/var/log/turnserver/turnserver.log")
    if log_file.exists():
        try:
            # Read last 1000 lines
            lines = log_file.read_text().splitlines()[-1000:]
            for line in lines:
                if "allocation created" in line.lower():
                    stats["total_allocations"] += 1
                if "allocation deleted" in line.lower():
                    stats["active_allocations"] = max(0, stats["total_allocations"] - 1)
                if "authentication failed" in line.lower():
                    stats["auth_failures"] += 1
        except Exception:
            pass

    return stats


