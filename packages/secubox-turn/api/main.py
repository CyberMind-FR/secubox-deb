@router.get("/stats")
async def stats(user=Depends(require_jwt)):
    """Get connection statistics."""
    sessions = _parse_sessions_from_cli()
    log_stats = _get_stats_from_logs()

    # Count connections by type
    udp_count = sum(1 for s in sessions if s.get("protocol", "").startswith("udp"))
    tcp_count = sum(1 for s in sessions if s.get("protocol", "").startswith("tcp"))

    return {
        "active_sessions": len(sessions),
        "udp_connections": udp_count,
        "tcp_connections": tcp_count,
        "total_allocations": log_stats["total_allocations"],
        "auth_failures": log_stats["auth_failures"],
        "total_data_relayed_bytes": log_stats["total_data_relayed"]
    }


@router.get("/sessions")
async def sessions(user=Depends(require_jwt)):
    """Get active sessions with client IPs."""
    sessions = _parse_sessions_from_cli()

    # Enrich with client info
    enriched = []
    for s in sessions:
        remote = s.get("remote_addr", "")
        if ":" in remote:
            parts = remote.rsplit(":", 1)
            client_ip = parts[0]
            client_port = parts[1] if len(parts) > 1 else ""
        else:
            client_ip = remote
            client_port = ""

        enriched.append({
            "client_ip": client_ip,
            "client_port": client_port,
            "protocol": s.get("protocol", ""),
            "local_addr": s.get("local_addr", ""),
            "state": s.get("state", "")
        })

    return {"sessions": enriched, "count": len(enriched)}


