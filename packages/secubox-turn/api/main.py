class UserRequest(BaseModel):
    username: str
    password: str


@router.post("/users")
async def add_user(req: UserRequest, user=Depends(require_jwt)):
    """Add a TURN user."""
    users = _get_users()

    # Check if user exists
    for u in users:
        if u.get("username") == req.username:
            return {"success": False, "error": "User already exists"}

    # Add user
    users.append({
        "username": req.username,
        "password": req.password,
        "created": int(time.time())
    })
    _save_users(users)

    # Also add to turnadmin if available
    realm = _get_realm()
    if Path(TURNADMIN_BIN).exists():
        _run_cmd([
            TURNADMIN_BIN, "-a", "-u", req.username,
            "-p", req.password, "-r", realm
        ])

    log.info("TURN user added: %s", req.username)
    return {"success": True, "message": f"User {req.username} added"}


@router.delete("/users/{username}")
async def delete_user(username: str, user=Depends(require_jwt)):
    """Delete a TURN user."""
    users = _get_users()
    new_users = [u for u in users if u.get("username") != username]

    if len(new_users) == len(users):
        return {"success": False, "error": "User not found"}

    _save_users(new_users)

    # Also remove from turnadmin if available
    realm = _get_realm()
    if Path(TURNADMIN_BIN).exists():
        _run_cmd([
            TURNADMIN_BIN, "-d", "-u", username, "-r", realm
        ])

    log.info("TURN user deleted: %s", username)
    return {"success": True, "message": f"User {username} deleted"}


