@router.get("/logs")
async def get_logs(lines: int = 100, user=Depends(require_jwt)):
    """Get TURN server logs."""
    log_file = Path("/var/log/turnserver/turnserver.log")

    if log_file.exists():
        try:
            all_lines = log_file.read_text().splitlines()
            return {"lines": all_lines[-lines:]}
        except Exception as e:
            return {"lines": [], "error": str(e)}

    # Try journalctl as fallback
    result = subprocess.run(
        ["journalctl", "-u", "coturn", "-n", str(lines), "--no-pager"],
        capture_output=True,
        text=True,
        timeout=10
    )
    return {"lines": result.stdout.splitlines()}


