class RealmRequest(BaseModel):
    realm: str
    secret: Optional[str] = None


@router.post("/realm")
async def set_realm(req: RealmRequest, user=Depends(require_jwt)):
    """Set realm and optionally regenerate secret."""
    if not TURNSERVER_CONF.exists():
        return {"success": False, "error": "Config file not found"}

    content = TURNSERVER_CONF.read_text()

    # Update realm
    if "realm=" in content:
        content = re.sub(r"realm=.*", f"realm={req.realm}", content)
    else:
        content += f"\nrealm={req.realm}\n"

    # Update secret if provided
    if req.secret:
        if "static-auth-secret=" in content:
            content = re.sub(r"static-auth-secret=.*", f"static-auth-secret={req.secret}", content)
        else:
            content += f"\nstatic-auth-secret={req.secret}\n"

    TURNSERVER_CONF.write_text(content)
    log.info("TURN realm updated: %s", req.realm)

    return {
        "success": True,
        "realm": req.realm,
        "message": "Restart service to apply changes"
    }


