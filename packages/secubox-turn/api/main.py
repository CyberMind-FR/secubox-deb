class ConfigRequest(BaseModel):
    content: str


@router.post("/config")
async def set_config(req: ConfigRequest, user=Depends(require_jwt)):
    """Update turnserver.conf configuration."""
    try:
        # Backup current config
        if TURNSERVER_CONF.exists():
            backup = TURNSERVER_CONF.with_suffix(".conf.bak")
            backup.write_text(TURNSERVER_CONF.read_text())

        # Write new config
        TURNSERVER_CONF.write_text(req.content)
        log.info("TURN config updated")

        return {"success": True, "message": "Config updated. Restart service to apply."}
    except Exception as e:
        log.error("Config update failed: %s", e)
        return {"success": False, "error": str(e)}


